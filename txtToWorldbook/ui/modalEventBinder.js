export function createModalEventBinder(deps) {
    const {
        bindModalBasicEventsUI,
        bindSettingEventsUI,
        bindCollapsePanelEventsUI,
        bindPromptEventsUI,
        bindMessageChainEventsUI,
        bindFileEventsUI,
        bindActionEventsUI,
        bindStreamEventsUI,
        bindExportEventsUI,
        EventDelegate,
        AppState,
        ErrorHandler,
        closeModal,
        showHelpModal,
        getEscKeyHandler,
        saveCurrentSettings,
        handleUseTavernApiChange,
        handleProviderChange,
        handleFetchModels,
        handleQuickTest,
        rechunkMemories,
        showAddCategoryModal,
        confirmAction,
        resetToDefaultCategories,
        renderCategoriesList,
        showAddDefaultEntryModal,
        saveDefaultWorldbookEntriesUI,
        applyDefaultWorldbookEntries,
        showResultSection,
        updateWorldbookPreview,
        testChapterRegex,
        renderMessageChainUI,
        handleFileSelect,
        handleClearFile,
        handleStartConversion,
        handleStopProcessing,
        handleRepairFailedMemories,
        showStartFromSelector,
        showProcessedResults,
        toggleMultiSelectMode,
        deleteSelectedMemories,
        updateMemoryQueueUI,
        showSearchModal,
        showReplaceModal,
        showWorldbookView,
        showHistoryView,
        showConsolidateCategorySelector,
        showCleanTagsModal,
        showAliasMergeUI,
        showTxtConverterPanel,
        showStoryOutlinePanel,
        showCurrentChapterPanel,
        updateStreamContent,
        showPromptPreview,
        showPlotOutlineConfigModal,
        importAndMergeWorldbook,
        loadTaskState,
        saveTaskState,
        exportSettings,
        importSettings,
        exportCharacterCard,
        exportVolumes,
        exportToSillyTavern,
        showMemoryContentModal,
    } = deps;

    function bindModalEvents(modalContainer) {
        bindModalBasicEventsUI({
            modalContainer,
            closeModal,
            showHelpModal,
            handleEscKey: getEscKeyHandler(),
        });

        bindSettingEventsUI({
            EventDelegate,
            modalContainer,
            AppState,
            saveCurrentSettings,
            handleUseTavernApiChange,
            handleProviderChange,
            handleFetchModels,
            handleQuickTest,
            rechunkMemories,
            showAddCategoryModal,
            confirmAction,
            resetToDefaultCategories,
            renderCategoriesList,
            showAddDefaultEntryModal,
            saveDefaultWorldbookEntriesUI,
            applyDefaultWorldbookEntries,
            showResultSection,
            updateWorldbookPreview,
            ErrorHandler,
            testChapterRegex,
        });

        bindCollapsePanelEventsUI();
        bindPromptEventsUI({ saveCurrentSettings });
        bindMessageChainEventsUI({
            AppState,
            renderMessageChainUI,
            saveCurrentSettings,
            confirmAction,
        });
        bindFileEventsUI({
            AppState,
            handleFileSelect,
            handleClearFile,
        });
        bindActionEventsUI({
            AppState,
            handleStartConversion,
            handleStopProcessing,
            handleRepairFailedMemories,
            showStartFromSelector,
            showProcessedResults,
            toggleMultiSelectMode,
            deleteSelectedMemories,
            updateMemoryQueueUI,
            showSearchModal,
            showReplaceModal,
            showWorldbookView,
            showHistoryView,
            showConsolidateCategorySelector,
            showCleanTagsModal,
            showAliasMergeUI,
            showTxtConverterPanel,
            showStoryOutlinePanel,
            showCurrentChapterPanel,
        });
        bindStreamEventsUI({ updateStreamContent });
        bindExportEventsUI({
            AppState,
            showPromptPreview,
            showPlotOutlineConfigModal,
            importAndMergeWorldbook,
            loadTaskState,
            saveTaskState,
            exportSettings,
            importSettings,
            exportCharacterCard,
            exportVolumes,
            exportToSillyTavern,
            showMemoryContentModal,
        });
    }

    return {
        bindModalEvents,
    };
}
